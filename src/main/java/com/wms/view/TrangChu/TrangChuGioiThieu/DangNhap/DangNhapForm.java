     private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String username = txtUsername.getText().trim();
    String password = new String(txtPassword.getPassword());

    if (username.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ tên đăng nhập và mật khẩu!");
        return;
    }

    DangNhapController controller = new DangNhapController();
    ketQuaDangNhap result = controller.login(username, password);

    switch (result) {
        case THANH_CONG:
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
            
            com.wms.model.NhanSu_KhachHang.NguoiDungDTO user = com.wms.controller.DangNhapController.getCurrentUser();
            
            java.awt.Window win = javax.swing.SwingUtilities.getWindowAncestor(this);
            if (win != null) win.dispose();
            
            // Điều hướng dựa trên vai trò
            if (user.hasRole("Quản lý")) {
                new com.wms.view.TrangChu.TrangChuQuanLy.TrangChuQuanLyForm().setVisible(true);
            } else if (user.hasRole("Hội viên")) {
                new com.wms.view.TrangChu.TrangChuHoiVien.TrangChuHoiVienForm().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Tài khoản chưa được phân quyền chính thức!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                new com.wms.view.TrangChu.TrangChuHoiVien.TrangChuHoiVienForm().setVisible(true);
            }
            break;
            
        case SAI_MAT_KHAU:
            JOptionPane.showMessageDialog(this, "Mật khẩu không chính xác!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            break;
            
        case KHONG_THAY_TAI_KHOAN:
            JOptionPane.showMessageDialog(this, "Tài khoản không tồn tại!", "Lỗi", JOptionPane.WARNING_MESSAGE);
            break;
            
        case TAI_KHOAN_KHONG_HOAT_DONG:
            JOptionPane.showMessageDialog(this, "Tài khoản của bạn đang bị khóa!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            break;
            
        case LOI_CSDL:
            JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu. Vui lòng thử lại sau!", "Lỗi hệ thống", JOptionPane.ERROR_MESSAGE);
            break;
    }
    }                                        

