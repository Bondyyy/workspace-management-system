    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {
        String maPhien = txtMaPhien.getText().trim();
        String tenDV = cboTenDichVu.getSelectedItem() != null ? cboTenDichVu.getSelectedItem().toString() : "";

        if (maPhien.isEmpty() || tenDV.isEmpty() || tenDV.contains("---")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dịch vụ cần xóa từ danh sách!", "Thông báo",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        int xacNhan = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa dịch vụ này khỏi phiên?",
                "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (xacNhan == JOptionPane.YES_OPTION) {
            String loi = controller.xoaDichVu(maPhien, tenDV);
            if (loi == null) {
                JOptionPane.showMessageDialog(this, "Xóa dịch vụ thành công!");
                hienThiDanhSachDichVu(maPhien);
                btnHuyActionPerformed(null);
            } else {
                JOptionPane.showMessageDialog(this, com.wms.util.ErrorMessageUtil.toUserMessage(loi), "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {
        String maPhien = txtMaPhien.getText().trim();
        String tenDV = cboTenDichVu.getSelectedItem() != null ? cboTenDichVu.getSelectedItem().toString() : "";
        int soLuong = (int) spinSoLuong.getValue();
        String ghiChu = txtGhiChu.getText().trim();

        if (maPhien.isEmpty() || tenDV.isEmpty() || tenDV.contains("---")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dịch vụ cần sửa!", "Thông báo",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        String loi = controller.capNhatDichVu(maPhien, tenDV, soLuong, ghiChu);
        if (loi == null) {
            JOptionPane.showMessageDialog(this, "Cập nhật dịch vụ thành công!");
            hienThiDanhSachDichVu(maPhien);
            btnHuyActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(this, com.wms.util.ErrorMessageUtil.toUserMessage(loi), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void tablePhienDangHoatDongMouseClicked(java.awt.event.MouseEvent evt) {
        int row = tablePhienDangHoatDong.getSelectedRow();
        if (row >= 0) {
            String maPhien = tablePhienDangHoatDong.getValueAt(row, 0).toString();
            String khachHang = tablePhienDangHoatDong.getValueAt(row, 1).toString();
            txtMaPhien.setText(maPhien);
            txtKhachHang.setText(khachHang);
            hienThiDanhSachDichVu(maPhien);
        }
    }

