    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {
        String maPhien = txtMaPhien.getText().trim();
        String loaiDV = cboLoaiDichVu.getSelectedItem() != null ? cboLoaiDichVu.getSelectedItem().toString().trim()
                : "";
        String tenDV = cboTenDichVu.getSelectedItem() != null ? cboTenDichVu.getSelectedItem().toString().trim() : "";
        int soLuong = (int) spinSoLuong.getValue();
        String ghiChu = txtGhiChu.getText().trim();

        if (maPhien.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã Phiên!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (loaiDV.isEmpty() || tenDV.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Loại và Tên Dịch Vụ!", "Cảnh báo",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (soLuong <= 0) {
            JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String loi = controller.themDichVu(maPhien, tenDV, soLuong, ghiChu);
        if (loi == null) {
            JOptionPane.showMessageDialog(this, "Thêm dịch vụ thành công!", "Thành công",
                    JOptionPane.INFORMATION_MESSAGE);
            hienThiDanhSachDichVu(maPhien);
            btnHuyActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(this, com.wms.util.ErrorMessageUtil.toUserMessage(loi), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void btnHuyActionPerformed(java.awt.event.ActionEvent evt) {
        txtMaPhien.setText("");
        if (cboLoaiDichVu.getItemCount() > 0)
            cboLoaiDichVu.setSelectedIndex(0);
        if (cboTenDichVu.getItemCount() > 0)
            cboTenDichVu.setSelectedIndex(0);
        spinSoLuong.setValue(1);
        txtGhiChu.setText("");
        tableDichVu.clearSelection();
    }

    private void tableDichVuMouseClicked(java.awt.event.MouseEvent evt) {
        int row = tableDichVu.getSelectedRow();
        if (row >= 0 && currentDichVuList != null && row < currentDichVuList.size()) {
            DichVuDaDatDTO dto = currentDichVuList.get(row);
            txtMaPhien.setText(dto.getMaPhien());
            txtKhachHang.setText(dto.getKhachHang());
            txtGhiChu.setText(dto.getGhiChu());
            spinSoLuong.setValue(dto.getSoLuong());

            // Tạm thời ngắt listener để tránh loop
            java.awt.event.ActionListener[] listeners = cboLoaiDichVu.getActionListeners();
            for (java.awt.event.ActionListener l : listeners)
                cboLoaiDichVu.removeActionListener(l);

            cboLoaiDichVu.setSelectedItem(dto.getTenLoaiDV());
            updateTenDichVu(); // Cập nhật danh sách tên theo loại
            cboTenDichVu.setSelectedItem(dto.getTenDichVu());

            // Đăng ký lại listener
            for (java.awt.event.ActionListener l : listeners)
                cboLoaiDichVu.addActionListener(l);
        }
    }

