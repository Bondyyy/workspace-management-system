    private String getMaCNDangChon() {
        String tenCN = (String) cbxChiNhanh.getSelectedItem();
        if (tenCN == null) return null;
        Object ma = cbxChiNhanh.getClientProperty("maCN_" + tenCN);
        return ma != null ? ma.toString() : null;
    }

    private String getMaVaiTroDangChon() {
        String tenVT = (String) cbxNhomQuyen.getSelectedItem();
        if (tenVT == null) return null;
        Object ma = cbxNhomQuyen.getClientProperty("maVT_" + tenVT);
        return ma != null ? ma.toString() : null;
    }

    private void setComboByValue(JComboBox<String> cbx, String value) {
        if (value == null) return;
        for (int i = 0; i < cbx.getItemCount(); i++) {
            if (value.equals(cbx.getItemAt(i))) { cbx.setSelectedIndex(i); return; }
        }
    }

    private void setComboByMaCN(String maCN) {
        if (maCN == null) return;
        for (int i = 1; i < cbxChiNhanh.getItemCount(); i++) {
            String ten = cbxChiNhanh.getItemAt(i);
            Object ma = cbxChiNhanh.getClientProperty("maCN_" + ten);
            if (maCN.equals(ma)) { cbxChiNhanh.setSelectedIndex(i); return; }
        }
    }

    private void setComboByMaVT(String maVT) {
        if (maVT == null) return;
        for (int i = 0; i < cbxNhomQuyen.getItemCount(); i++) {
            String ten = cbxNhomQuyen.getItemAt(i);
            Object ma = cbxNhomQuyen.getClientProperty("maVT_" + ten);
            if (maVT.equals(ma)) { cbxNhomQuyen.setSelectedIndex(i); return; }
        }
    }


