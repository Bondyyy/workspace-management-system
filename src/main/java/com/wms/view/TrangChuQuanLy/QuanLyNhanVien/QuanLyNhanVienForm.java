    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {
        String hoTen = txtHoTen.getText().trim();
        String sdt   = txtSDT.getText().trim();
        String email = txtEmail.getText().trim();
        if (hoTen.isEmpty() || sdt.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ họ tên, SĐT và email!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (cbxChiNhanh.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn chi nhánh!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        NhanVienDTO nv = new NhanVienDTO();
        nv.setLoaiNV((String) cbxLoaiNV.getSelectedItem());
        nv.setCaLamViec((String) cbxCaLam.getSelectedItem());
        nv.setTrangThaiLamViec((String) cbxTrangThai.getSelectedItem());
        nv.setMaCN(getMaCNDangChon());
        try { nv.setLuongCoBan(Double.parseDouble(txtLuong.getText().trim())); } catch (NumberFormatException e) { nv.setLuongCoBan(0.0); }

        NguoiDungDTO nd = new NguoiDungDTO();
        nd.setEmail(email);
        nd.setSdt(sdt);
        nd.setGioiTinh((String) cbxGioiTinh.getSelectedItem());
        Object anhPath = lblAnhDaiDien.getClientProperty("anhPath");
        nd.setAnhDaiDien(anhPath != null ? anhPath.toString() : null);

        String maVaiTro = getMaVaiTroDangChon();
        boolean ok = nhanVienDAO.themNhanVien(nv, nd, hoTen, maVaiTro);
        if (ok) {
            JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            lamMoiForm();
            loadData();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm nhân viên thất bại! Kiểm tra lại thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

