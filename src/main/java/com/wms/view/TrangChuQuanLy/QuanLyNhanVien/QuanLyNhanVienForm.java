    private void loadData() {
        List<Object[]> ds = nhanVienDAO.layDanhSachNhanVien();
        hienThiLenBang(ds);
    }

    private void hienThiLenBang(List<Object[]> ds) {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        int row = 0;
        for (Object[] r : ds) {
            model.addRow(new Object[]{
                r[0], r[1], r[2], r[3], r[4], r[5], r[6], r[7]
            });
            tblNhanVien.putClientProperty("maCN_" + row, r[11]);
            tblNhanVien.putClientProperty("maVT_" + row, r[12]);
            tblNhanVien.putClientProperty("maND_" + row, r[13]);
            tblNhanVien.putClientProperty("gioiTinh_" + row, r[8]);
            tblNhanVien.putClientProperty("email_" + row, r[9]);
            tblNhanVien.putClientProperty("luong_" + row, r[10] != null ? String.valueOf(r[10]) : "");
            tblNhanVien.putClientProperty("trangThai_" + row, r[7]);
            row++;
        }
    }

    private void loadComboBoxData() {
        cbxChiNhanh.removeAllItems();
        cbxChiNhanh.addItem("-- Chọn chi nhánh --");
        List<String[]> chiNhanhs = nhanVienDAO.layDanhSachChiNhanh();
        for (String[] cn : chiNhanhs) {
            cbxChiNhanh.addItem(cn[1]);
            cbxChiNhanh.putClientProperty("maCN_" + cn[1], cn[0]);
        }
        cbxNhomQuyen.removeAllItems();
        List<String[]> vaiTros = nhanVienDAO.layDanhSachVaiTro();
        for (String[] vt : vaiTros) {
            cbxNhomQuyen.addItem(vt[1]);
            cbxNhomQuyen.putClientProperty("maVT_" + vt[1], vt[0]);
        }
    }

