    private void tblKhongGianMouseClicked(java.awt.event.MouseEvent evt) {
        int row = tblKhongGian.getSelectedRow();
        if (row < 0)
            return;

        String maKG = tblKhongGian.getValueAt(row, 0).toString();
        String tenKG = tblKhongGian.getValueAt(row, 1).toString();
        String maCN = tblKhongGian.getValueAt(row, 2).toString();
        String maLoaiKG = tblKhongGian.getValueAt(row, 3).toString();
        String trangThai = tblKhongGian.getValueAt(row, 5).toString();

        txtMaKhongGian.setText(maKG);
        txtTenKhongGian.setText(tenKG);

        cbxTrangThai.setSelectedItem(trangThai);

        for (int i = 0; i < cbxChiNhanh.getItemCount(); i++) {
            String item = cbxChiNhanh.getItemAt(i);
            if (item != null && item.contains(" - ")) {
                String tenCN = item.split(" - ", 2)[1].trim();
                if (tenCN.equalsIgnoreCase(maCN)) {
                    cbxChiNhanh.setSelectedIndex(i);
                    break;
                }
            }
        }
        for (int i = 0; i < cbxLoaiKhongGian.getItemCount(); i++) {
            String item = cbxLoaiKhongGian.getItemAt(i);
            if (item != null && item.contains(" - ")) {
                String tenLoai = item.split(" - ", 2)[1].trim();
                if (tenLoai.equalsIgnoreCase(maLoaiKG)) {
                    cbxLoaiKhongGian.setSelectedIndex(i);
                    break;
                }
            }
        }
    }

