    private void apDungPhanQuyen() {
        com.wms.model.TrangChuQuanLy.QuanLyNguoiDung.NguoiDungDTO user = com.wms.controller.TrangChuGioiThieu.DangNhapController
                .getCurrentUser();
        if (user == null)
            return;
        if (!user.hasRole(com.wms.config.AppConstants.ROLE_ADMIN_CODE)) {
            maCNFilter = new com.wms.dao.TrangChuQuanLy.QuanLyNhanVien.NhanVienDAO().layMaCNTuMaND(user.getMaND());
            if (maCNFilter != null) {
                for (int i = 0; i < cbxChiNhanh.getItemCount(); i++) {
                    String item = cbxChiNhanh.getItemAt(i);
                    if (item != null && item.startsWith(maCNFilter + " - ")) {
                        cbxChiNhanh.setSelectedIndex(i);
                        break;
                    }
                }
                cbxChiNhanh.setEnabled(false);
            }
        }
    }

    private void setupTable() {
        javax.swing.table.DefaultTableCellRenderer centerRenderer = new javax.swing.table.DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(javax.swing.JLabel.CENTER);
        for (int i = 0; i < tblKhongGian.getColumnCount(); i++) {
            if (i == 0 || i >= 4) { // Mã, Tọa độ, Trạng thái
                tblKhongGian.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
            }
        }
    }

    private void napComboBoxChiNhanh() {
        cbxChiNhanh.removeAllItems();
        cbxChiNhanh.addItem("-- Chọn chi nhánh --");
        List<ChiNhanhDTO> dsCN = controller.layDanhSachChiNhanh();
        for (ChiNhanhDTO cn : dsCN) {
            cbxChiNhanh.addItem(cn.getMaCN() + " - " + cn.getTenCN());
        }
    }

    private void napComboBoxLoaiKhongGian() {
        cbxLoaiKhongGian.removeAllItems();
        cbxLoaiKhongGian.addItem("-- Chọn loại --");
        List<LoaiKhongGianDTO> dsLoai = controller.layTatCaLoai();
        for (LoaiKhongGianDTO loai : dsLoai) {
            cbxLoaiKhongGian.addItem(loai.getMaLoaiKG() + " - " + loai.getTenLoaiKG());
        }
    }

    private void taiDanhSach(String tuKhoa) {
        DefaultTableModel model = (DefaultTableModel) tblKhongGian.getModel();
        model.setRowCount(0);
        String maCN = getMaFromCombo(cbxChiNhanh);
        String maLoai = getMaFromCombo(cbxLoaiKhongGian);
        List<KhongGianDTO> list = controller.timKiemKhongGian(tuKhoa, maCN, maLoai);
        for (KhongGianDTO dto : list) {
            String viTriHienThi = String.format("%d,%d,%d,%d",
                    dto.getToaDoX(), dto.getToaDoY(), dto.getChieuDai(), dto.getChieuRong());

            String tenCN = (dto.getTenCN() != null) ? dto.getTenCN() : dto.getMaCN();
            String tenLoai = (dto.getTenLoaiKG() != null) ? dto.getTenLoaiKG() : dto.getMaLoaiKG();

            model.addRow(new Object[] {
                    dto.getMaKG(),
                    dto.getTenKG(),
                    tenCN,
                    tenLoai,
                    viTriHienThi,
                    dto.getTrangThaiKG()
            });
        }
    }

    private String getMaFromCombo(javax.swing.JComboBox<String> cbx) {
        Object item = cbx.getSelectedItem();
        if (item == null)
            return null;
        String str = item.toString();
        if (str.startsWith("--"))
            return null;
        return str.split(" - ")[0].trim();
    }

