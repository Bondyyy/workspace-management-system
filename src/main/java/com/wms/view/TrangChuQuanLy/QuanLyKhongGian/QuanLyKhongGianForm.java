    private void btnSoDoActionPerformed(java.awt.event.ActionEvent evt) {
        String maCN = getMaFromCombo(cbxChiNhanh);
        if (maCN == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn chi nhánh cần xem sơ đồ từ danh sách chọn!");
            return;
        }

        String tenCN = "";
        Object item = cbxChiNhanh.getSelectedItem();
        if (item != null) {
            String[] parts = item.toString().split(" - ", 2);
            if (parts.length > 1) {
                tenCN = parts[1].trim();
            }
        }

        java.awt.Window parentWindow = javax.swing.SwingUtilities.getWindowAncestor(this);
        java.awt.Frame parentFrame = null;
        if (parentWindow instanceof java.awt.Frame) {
            parentFrame = (java.awt.Frame) parentWindow;
        }

        QuanLySoDoKhongGianForm dialog = new QuanLySoDoKhongGianForm(parentFrame, true, maCN, tenCN);
        dialog.setVisible(true);
    }

    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {
        String tenKG = txtTenKhongGian.getText().trim();
        if (tenKG.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Tên không gian!", "Cảnh báo",
                    JOptionPane.WARNING_MESSAGE);
            txtTenKhongGian.requestFocus();
            return;
        }

        String maCN = getMaFromCombo(cbxChiNhanh);
        String maLoaiKG = getMaFromCombo(cbxLoaiKhongGian);

        if (maCN == null || maLoaiKG == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Chi nhánh và Loại không gian!", "Cảnh báo",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        KhongGianDTO dto = new KhongGianDTO();
        dto.setMaKG(txtMaKhongGian.getText().trim());
        dto.setTenKG(tenKG);
        dto.setTrangThaiKG(cbxTrangThai.getSelectedItem().toString());
        dto.setMaLoaiKG(maLoaiKG);
        dto.setMaCN(maCN);
        // Default coordinates
        dto.setToaDoX(1);
        dto.setToaDoY(1);
        dto.setChieuDai(1);
        dto.setChieuRong(1);

        if (controller.themKhongGian(dto)) {
            JOptionPane.showMessageDialog(this, "Thêm không gian thành công!", "Thành công",
                    JOptionPane.INFORMATION_MESSAGE);
            taiDanhSach(null);
            laMoiForm();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm không gian thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {
        String maKG = txtMaKhongGian.getText().trim();
        if (maKG.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 không gian từ bảng!", "Thông báo",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        String tenKG = txtTenKhongGian.getText().trim();
        if (tenKG.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Tên không gian!", "Cảnh báo",
                    JOptionPane.WARNING_MESSAGE);
            txtTenKhongGian.requestFocus();
            return;
        }

        String maCN = getMaFromCombo(cbxChiNhanh);
        String maLoaiKG = getMaFromCombo(cbxLoaiKhongGian);

        if (maCN == null || maLoaiKG == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Chi nhánh và Loại không gian!", "Cảnh báo",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        int xacNhan = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn cập nhật không gian \"" + maKG + "\"?",
                "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (xacNhan != JOptionPane.YES_OPTION)
            return;

        KhongGianDTO dto = controller.layKhongGianTheoMa(maKG);
        if (dto == null)
            dto = new KhongGianDTO();

        dto.setMaKG(maKG);
        dto.setTenKG(tenKG);
        dto.setTrangThaiKG(cbxTrangThai.getSelectedItem().toString());
        dto.setMaLoaiKG(maLoaiKG);
        dto.setMaCN(maCN);

        if (controller.capNhatKhongGian(dto)) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            taiDanhSach(null);
            laMoiForm();
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

