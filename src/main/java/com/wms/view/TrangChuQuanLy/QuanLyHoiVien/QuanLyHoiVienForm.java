        private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {                                           
        com.wms.model.NhanSu_KhachHang.HoiVienDTO newHoiVien = new com.wms.model.NhanSu_KhachHang.HoiVienDTO();
        newHoiVien.setHoTen(txtHoTen.getText().trim());
        newHoiVien.setSdt(txtSDT.getText().trim());
        newHoiVien.setEmail(txtEmail.getText().trim());
        
        try {
            String dateStr = txtNgaySinh.getText().trim();
            if (!dateStr.isEmpty()) {
                java.util.Date parsed = new java.text.SimpleDateFormat("dd/MM/yyyy").parse(dateStr);
                newHoiVien.setNgaySinh(new java.sql.Date(parsed.getTime()));
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ (dd/MM/yyyy)!", "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            controller.themMoiHoiVien(newHoiVien);
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm mới thành công!\nTài khoản đăng nhập: SĐT hoặc Email\nMật khẩu mặc định: 123456");
            loadDataToTable();
            btnHuyActionPerformed(null);
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm mới thất bại!\nChi tiết: " + ex.getMessage(), "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }                                          

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {                                           
        if (selectedHoiVien == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn một hội viên để cập nhật!", "Thông báo", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        selectedHoiVien.setHoTen(txtHoTen.getText().trim());
        selectedHoiVien.setSdt(txtSDT.getText().trim());
        selectedHoiVien.setEmail(txtEmail.getText().trim());
        try {
            String dateStr = txtNgaySinh.getText().trim();
            if (!dateStr.isEmpty()) {
                java.util.Date parsed = new java.text.SimpleDateFormat("dd/MM/yyyy").parse(dateStr);
                selectedHoiVien.setNgaySinh(new java.sql.Date(parsed.getTime()));
            } else {
                selectedHoiVien.setNgaySinh(null);
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ (dd/MM/yyyy)!", "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            controller.capNhatHoiVien(selectedHoiVien);
            javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            loadDataToTable();
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thất bại!\nChi tiết: " + ex.getMessage(), "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }                                          

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {                                       
        if (selectedHoiVien == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn một hội viên để xóa!", "Thông báo", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa hội viên này?", "Xác nhận", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                controller.xoaHoiVien(selectedHoiVien.getMaKH(), selectedHoiVien.getMaND());
                javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công!");
                btnHuyActionPerformed(null);
                loadDataToTable();
            } catch (Exception ex) {
                javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại!\nChi tiết: " + ex.getMessage(), "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }                                      

    private void btnHuyActionPerformed(java.awt.event.ActionEvent evt) {                                       
        selectedHoiVien = null;
        txtHoTen.setText("");
        txtSDT.setText("");
        txtEmail.setText("");
        txtNgaySinh.setText("");
        cbxHangThanhVien.setSelectedIndex(0);
        tblHoiVien.clearSelection();
    }                                      

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {                                           
        hoiVienList = controller.timKiemHoiVien(txtTimKiem.getText().trim());
        fillTable();
    }                                          

    private void tblHoiVienMouseClicked(java.awt.event.MouseEvent evt) {                                         
        int index = tblHoiVien.getSelectedRow();
        if (index >= 0 && index < hoiVienList.size()) {
            selectedHoiVien = hoiVienList.get(index);
            txtHoTen.setText(selectedHoiVien.getHoTen());
            txtSDT.setText(selectedHoiVien.getSdt());
            txtEmail.setText(selectedHoiVien.getEmail());
            if (selectedHoiVien.getNgaySinh() != null) {
                txtNgaySinh.setText(new java.text.SimpleDateFormat("dd/MM/yyyy").format(selectedHoiVien.getNgaySinh()));
            } else {
                txtNgaySinh.setText("");
            }
            if (selectedHoiVien.getHangThanhVien() != null) {
                cbxHangThanhVien.setSelectedItem(selectedHoiVien.getHangThanhVien());
            }
        }
    }

