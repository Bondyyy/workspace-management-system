    private void tblHoiVienMouseClicked(java.awt.event.MouseEvent evt) {
        int index = tblHoiVien.getSelectedRow();
        if (index >= 0 && index < hoiVienList.size()) {
            selectedHoiVien = hoiVienList.get(index);
            txtHoTen.setText(selectedHoiVien.getHoTen());
            txtSDT.setText(selectedHoiVien.getSdt());
            txtEmail.setText(selectedHoiVien.getEmail());
            txtNgaySinh.setText(com.wms.util.DateInputUtil.formatDate(selectedHoiVien.getNgaySinh()));
            cbxHangTV.setSelectedItem(selectedHoiVien.getHangThanhVien());
            cbxGioiTinh.setSelectedItem(selectedHoiVien.getGioiTinh());
            cbxTrangThai.setSelectedItem(selectedHoiVien.getTrangThai());
            txtTongChiTieu.setText(com.wms.util.InputFormatUtil.formatThousands(selectedHoiVien.getTongChiTieu()));
            txtMaHV.setText(selectedHoiVien.getMaKH());

            if (selectedHoiVien.getAnhDaiDien() != null) {
                currentAvatarData = selectedHoiVien.getAnhDaiDien();
                try {
                    ImageIcon icon = new ImageIcon(currentAvatarData);
                    Image img = icon.getImage().getScaledInstance(lblAnhDaiDien.getWidth(), lblAnhDaiDien.getHeight(),
                            Image.SCALE_SMOOTH);
                    lblAnhDaiDien.setIcon(new ImageIcon(img));
                    lblAnhDaiDien.setText("");
                } catch (Exception e) {
                    lblAnhDaiDien.setIcon(null);
                    lblAnhDaiDien.setText("[Lỗi ảnh]");
                }
            } else {
                currentAvatarData = null;
                lblAnhDaiDien.setIcon(null);
                lblAnhDaiDien.setText("[Ảnh 3x4]");
            }
        }
    }

