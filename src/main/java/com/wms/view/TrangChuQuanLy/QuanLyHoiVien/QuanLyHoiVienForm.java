    private void btnDoiAnhActionPerformed(java.awt.event.ActionEvent evt) {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Images", "jpg", "png", "jpeg"));
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            try {
                File file = fileChooser.getSelectedFile();
                currentAvatarData = java.nio.file.Files.readAllBytes(file.toPath());
                ImageIcon icon = new ImageIcon(currentAvatarData);
                Image img = icon.getImage().getScaledInstance(lblAnhDaiDien.getWidth(), lblAnhDaiDien.getHeight(),
                        Image.SCALE_SMOOTH);
                lblAnhDaiDien.setIcon(new ImageIcon(img));
                lblAnhDaiDien.setText("");
            } catch (java.io.IOException ex) {
                JOptionPane.showMessageDialog(this, "Lỗi đọc file ảnh!");
            }
        }
    }

    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            HoiVienDTO dto = getFormData();
            controller.themMoiHoiVien(dto);
            JOptionPane.showMessageDialog(this, "Thêm mới thành công!\nTài khoản: SĐT hoặc Email\nMật khẩu: 123456");
            laMoiForm();
            refreshTableTheoDieuKienHienTai();
        } catch (Exception ex) {
            com.wms.util.MessageUtil.showError(this, ex.getMessage(), ex);
        }
    }

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {
        if (selectedHoiVien == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hội viên!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            HoiVienDTO dto = getFormData();
            controller.capNhatHoiVien(dto);
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            laMoiForm();
            refreshTableTheoDieuKienHienTai();
        } catch (Exception ex) {
            com.wms.util.MessageUtil.showError(this, ex.getMessage(), ex);
        }
    }

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {
        if (selectedHoiVien == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hội viên!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int xacNhan = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa?", "Xác nhận",
                JOptionPane.YES_NO_OPTION);
        if (xacNhan == JOptionPane.YES_OPTION) {
            try {
                controller.xoaHoiVien(selectedHoiVien.getMaKH(), selectedHoiVien.getMaND());
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
                laMoiForm();
                refreshTableTheoDieuKienHienTai();
            } catch (Exception ex) {
                com.wms.util.MessageUtil.showError(this, ex.getMessage(), ex);
            }
        }
    }

