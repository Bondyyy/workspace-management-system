    private void fillTable() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblHoiVien.getModel();
        model.setRowCount(0);
        for (com.wms.model.NhanSu_KhachHang.HoiVienDTO dto : hoiVienList) {
            if (dto.getNgaySinh() != null) {
                new java.text.SimpleDateFormat("dd/MM/yyyy").format(dto.getNgaySinh());
            }
            model.addRow(new Object[]{
                dto.getMaKH(), dto.getHoTen(), dto.getSdt(), dto.getEmail(), dto.getHangThanhVien()
            });
        }
    }
