    private void fillTable() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblHangTV.getModel();
        model.setRowCount(0);
        if (hangList != null) {
            for (com.wms.model.HangThanhVienDTO dto : hangList) {
                model.addRow(new Object[]{
                    dto.getMaHangThanhVien(), dto.getTenHangThanhVien(), dto.getPhanTramTienGiam(), dto.getTongChiTieuToiThieu()
                });
            }
        }
    }

