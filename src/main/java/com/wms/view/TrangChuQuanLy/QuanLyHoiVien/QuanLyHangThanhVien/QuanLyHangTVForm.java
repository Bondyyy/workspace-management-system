    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {
        com.wms.model.HangThanhVienDTO dto = new com.wms.model.HangThanhVienDTO();
        dto.setMaHangThanhVien(txtMaHang.getText().trim());
        dto.setTenHangThanhVien(cbxTenHang.getSelectedItem().toString());
        dto.setPhanTramTienGiam(Double.valueOf(spnPhanTram.getValue().toString()));
        
        try {
            double chiTieu = Double.parseDouble(txtChiTieu.getText().trim());
            dto.setTongChiTieuToiThieu(chiTieu);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Chi tiêu tối thiểu không hợp lệ!", "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            controller.themHang(dto);
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm mới thành công!");
            loadDataToTable();
            btnLamMoiActionPerformed(null);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {
        if (selectedHang == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn hạng thành viên cần sửa!");
            return;
        }
        selectedHang.setTenHangThanhVien(cbxTenHang.getSelectedItem().toString());
        selectedHang.setPhanTramTienGiam(Double.valueOf(spnPhanTram.getValue().toString()));
        try {
            double chiTieu = Double.parseDouble(txtChiTieu.getText().trim());
            selectedHang.setTongChiTieuToiThieu(chiTieu);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Chi tiêu tối thiểu không hợp lệ!", "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            controller.capNhatHang(selectedHang);
            javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            loadDataToTable();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {
        if (selectedHang == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn hạng thành viên cần xóa!");
            return;
        }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa hạng này?", "Xác nhận", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                controller.xoaHang(selectedHang.getMaHangThanhVien());
                javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công!");
                loadDataToTable();
                btnLamMoiActionPerformed(null);
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void btnLamMoiActionPerformed(java.awt.event.ActionEvent evt) {
        txtMaHang.setText("");
        cbxTenHang.setSelectedIndex(0);
        spnPhanTram.setValue(0.0);
        txtChiTieu.setText("");
        selectedHang = null;
        tblHangTV.clearSelection();
    }

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {
        hangList = controller.timKiemHang(txtTimKiem.getText().trim());
        fillTable();
    }

    private void tblHangTVMouseClicked(java.awt.event.MouseEvent evt) {
        int idx = tblHangTV.getSelectedRow();
        if (idx >= 0 && idx < hangList.size()) {
            selectedHang = hangList.get(idx);
            txtMaHang.setText(selectedHang.getMaHangThanhVien());
            cbxTenHang.setSelectedItem(selectedHang.getTenHangThanhVien());
            spnPhanTram.setValue(selectedHang.getPhanTramTienGiam() != null ? selectedHang.getPhanTramTienGiam() : 0.0);
            txtChiTieu.setText(selectedHang.getTongChiTieuToiThieu() != null ? String.format("%.0f", selectedHang.getTongChiTieuToiThieu()) : "0");
        }
    }

