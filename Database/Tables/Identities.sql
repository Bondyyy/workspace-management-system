-- 1. Bảng Users
CREATE TABLE Users (
    user_id      NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    
    -- Thông tin đăng nhập
    username      VARCHAR2(50) UNIQUE NOT NULL,
    password_hash VARCHAR2(255) NOT NULL,
    email         VARCHAR2(150) UNIQUE NOT NULL,
    status        VARCHAR2(20) DEFAULT 'ACTIVE',

    -- Thông tin cá nhân
    full_name     VARCHAR2(100) NOT NULL,
    phone_number  VARCHAR2(20),
    avatar_url    VARCHAR2(255),    -- Để thêm hình avt
    gender        VARCHAR2(10),     -- Nam/Nữ/Khác
    birthday      DATE,             -- Ngay sinh nhật thì có thể thêm khuyến mãi
    address       VARCHAR2(255),
    
    -- Theo dõi hệ thống
    last_login_at TIMESTAMP,                       -- Lần cuối đăng nhập
    created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    is_deleted    NUMBER(1) DEFAULT 0 CHECK (is_deleted IN (0,1)) -- Chỉnh thành xóa hay chưa thoi, thay vì xóa khi nào so với trước đó
);

-- 2. Bảng Roles -- Tên các vai trò
CREATE TABLE Roles (
    role_id       NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_name     VARCHAR2(100) NOT NULL UNIQUE,
    description   VARCHAR2(255),
    is_system_role NUMBER(1) DEFAULT 0,            -- Nếu = 1 thì là role mặc định hệ thống, ko được xóa (tránh xóa quyền Admin,...)
    
    created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    is_active     NUMBER(1) DEFAULT 1,
    is_deleted    NUMBER(1) DEFAULT 0 CHECK (is_deleted IN (0,1))
);

-- 3. Bảng FUNCTIONS -- Bảng này để chia ra các phần quản lý, vd: Lễ tân thì làm việc với phần quản lý Booking
CREATE TABLE Functions (
    function_id   NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    function_code VARCHAR2(50) UNIQUE NOT NULL,    -- Mã code (vd: FUNC_BOOKING)
    function_name VARCHAR2(100) NOT NULL,          -- Tên hiển thị
    module_name   VARCHAR2(50),                    -- Gom nhóm
    
    created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    is_deleted    NUMBER(1) DEFAULT 0 CHECK (is_deleted IN (0,1))
);

-- 4. Bảng PERMISSIONS -- Chi tiết các quyền coi, thêm, sửa, xóa với các phần qly, vd: Lễ tân thì đc xem thêm xóa sửa Booking
CREATE TABLE Permissions (
    permission_id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    function_id   NUMBER(10) NOT NULL,             -- Thuộc chức năng nào
    
    -- Ma trận phân quyền (1: Có quyền, 0: Không)
    can_view      NUMBER(1) DEFAULT 0 CHECK (can_view IN (0,1)),
    can_add       NUMBER(1) DEFAULT 0 CHECK (can_add IN (0,1)),
    can_edit      NUMBER(1) DEFAULT 0 CHECK (can_edit IN (0,1)),
    can_delete    NUMBER(1) DEFAULT 0 CHECK (can_delete IN (0,1)),
    
    created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    is_deleted    NUMBER(1) DEFAULT 0 CHECK (is_deleted IN (0,1))
);

-- 5. Bảng ROLE_PERMISSIONS -- Vai trò gì được quyền gì
CREATE TABLE RolePermissions (
    role_id       NUMBER(10) NOT NULL,
    permission_id NUMBER(10) NOT NULL,
    assigned_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (role_id, permission_id)       
);

-- 6. Bảng USER_ROLES -- User nào giữ vai trò nào
CREATE TABLE UserRoles (
    user_id       NUMBER(10) NOT NULL,
    role_id       NUMBER(10) NOT NULL,
    assigned_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (user_id, role_id)             
);

-- 7. Bảng CUSTOMERS 
CREATE TABLE Customers (
    customer_id       NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    user_id           NUMBER(10) NOT NULL UNIQUE,  
    membership_tier_id NUMBER,                     
    
    customer_code     VARCHAR2(50) UNIQUE,
    loyalty_points    NUMBER DEFAULT 0,
    lifetime_spending NUMBER(15, 2) DEFAULT 0,
    company_info      VARCHAR2(255),
    
    created_at        TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at        TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    is_deleted        NUMBER(1) DEFAULT 0 CHECK (is_deleted IN (0,1))
);

-- 8. Bảng EMPLOYEES 
CREATE TABLE Employees (
    employee_id      NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    user_id          NUMBER(10) NOT NULL UNIQUE,   
    employee_code    VARCHAR2(50) UNIQUE NOT NULL,
    branch_id        NUMBER,                       
    
    position         VARCHAR2(100),
    hire_date        DATE,
    contract_type    VARCHAR2(50),
    bank_number      VARCHAR2(50),                
    job_status       VARCHAR2(50) DEFAULT 'ACTIVE',
    identity_card    VARCHAR2(20) UNIQUE,          -- CCCD/CMND 
    
    created_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    is_deleted       NUMBER(1) DEFAULT 0 CHECK (is_deleted IN (0,1))
);

ALTER TABLE Users ADD CONSTRAINT chk_user_status CHECK (status IN ('ACTIVE', 'BANNED', 'LOCKED'));
ALTER TABLE Employees ADD CONSTRAINT chk_emp_status CHECK (job_status IN ('ACTIVE', 'RESIGNED', 'ON_LEAVE'));
ALTER TABLE Customers ADD CONSTRAINT chk_cust_points CHECK (loyalty_points >= 0);